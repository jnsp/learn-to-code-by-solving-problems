from cow_gym import count_consistent, is_better, pairs


def test_pairs():
    n_cows = 4
    assert list(pairs(n_cows)) == [(1, 2), (1, 3), (1, 4), (2, 3), (2, 4), (3, 4)]


def test_rank():
    pair = (3, 4)
    record = (4, 1, 2, 3)
    assert not is_better(pair, record)


def test_count_consistent():
    n_cows = 4
    records = [(4, 1, 2, 3), (4, 1, 3, 2), (4, 2, 1, 3)]
    assert count_consistent(n_cows, records) == 4


def test_count_consistent2():
    n_cows = 20
    records = [(17, 6, 5, 12, 7, 11, 14, 15, 18, 16, 20, 4, 13, 9, 10, 3, 8, 2, 19, 1)]
    assert count_consistent(n_cows, records) == 190


